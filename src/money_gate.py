"""Ворота денег — пре-регистрация 05.07.2026 (решение кванта).

Цель проекта — сигналы, которые зарабатывают. Переход от paper к реальному
капиталу должен происходить по условию, зафиксированному ДО его достижения,
а не по настроению. Условия открытия ворот (LONG-сторона):

  1. n >= 15 закрытых оценённых LONG-сигналов;
  2. avgR >= +0.4;
  3. медиана R > 0 — среднее нельзя купить одним жирным выбросом: при n=15
     и типичном разбросе R порог по среднему — лишь ~1.3 SE от нуля, один
     +6R-хвост протаскивает ворота на шуме; медиана требует, чтобы
     большинство сделок было плюсовым.

SHORT-сторона остаётся paper до вердикта теневой выборки SHORT_RALLY_IN_BEAR
(конец июля: N>=15 эпизодов, fwd72 <= -4% в >=60% случаев — см. verdict_journal).

Алерт разовый (дедуп через state), автоторговли нет: советник советует,
деньги включает оператор. Рекомендация сайзинга внутри алерта — тоже часть
пре-регистрации: риск 1% выделенного капитала на сделку, стоп из сигнала.
"""
from __future__ import annotations

import statistics
from typing import Dict, List

GATE_N = 15
GATE_AVG_R = 0.4


def check_gate(rows: List[Dict]) -> Dict:
    """Состояние ворот по закрытым оценённым LONG-сигналам.

    rows — записи с direction и r_multiple (None = не оценён, пропускается).
    Чистая функция: вся политика здесь, тестируется без сети и файлов."""
    rs = [float(r["r_multiple"]) for r in rows
          if r.get("direction") == "LONG" and r.get("r_multiple") is not None]
    n = len(rs)
    avg_r = sum(rs) / n if n else 0.0
    median_r = statistics.median(rs) if n else 0.0
    is_open = n >= GATE_N and avg_r >= GATE_AVG_R and median_r > 0
    return {"open": is_open, "n": n, "avg_r": round(avg_r, 3),
            "median_r": round(median_r, 3)}


def format_gate_alert(gate: Dict) -> str:
    return (
        "\U0001f513 <b>ВОРОТА ДЕНЕГ ОТКРЫТЫ (LONG)</b>\n"
        f"Закрытых LONG-сигналов: {gate['n']} · avgR {gate['avg_r']:+.2f} · "
        f"медиана {gate['median_r']:+.2f}\n"
        f"<i>Пре-регистрированное условие (n\u2265{GATE_N}, avgR\u2265+{GATE_AVG_R}, "
        "медиана>0) выполнено. Рекомендация, зафиксированная заранее: "
        "пополнить счёт, торговать ТОЛЬКО LONG-вердикты, риск 1% выделенного "
        "капитала на сделку, стоп из сигнала, гвард — исполнительная "
        "дисциплина. SHORT остаётся paper до вердикта теневой выборки.</i>")


def format_gate_progress(gate: Dict) -> str:
    """Строка прогресса для недельного KPI, пока ворота закрыты."""
    return (f"Ворота денег (LONG): {gate['n']}/{GATE_N} · "
            f"avgR {gate['avg_r']:+.2f} (нужно \u2265+{GATE_AVG_R}) · "
            f"медиана {gate['median_r']:+.2f} (нужно >0)")
